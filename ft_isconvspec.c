int		ft_isconvspec(char c)
{
	if (c == 's' || c == 'S' || c == 'p' || c == 'd' || c == 'D' || c == 'i' ||
		c == 'o' || c == 'u' || c == 'U' || c == 'x' || c == 'X' || c == 'c' ||
		c == 'C')
		return (1);
	return (0);
}
