#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	int		i;

	if (s == NULL)
	{
		return ;
	}
	i = 0;
	while (s[i] != '\0')
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, "\n", 1);
}
